package Classes;


public class Aveji {
    public String masala;
    public String compania;
    public String tipi;

    public void Aveji(String masala, String compania, String tipi) {
        this.masala = masala;
        this.compania = compania;
        this.tipi = tipi;
    }

    public void setMasala(String masala) {
        this.masala = masala;
    }

    public String getMasala() {
        return masala;
    }

    public void setCompania(String compania) {
        this.compania = compania;
    }

    public String getCompania() {
        return compania;
    }

}
